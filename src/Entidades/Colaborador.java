public class Colaborador {
    
    
    private double salarioHora, horasTrabajadas, extrasCorrientes, extrasDobles;
   

    public double getSalarioHora() {
        return salarioHora;
    }

    public void setSalarioHora(double salarioHora) {
        this.salarioHora = salarioHora;
    }

    public double getHorasTrabajadas() {
        return horasTrabajadas;
    }

    public void setHorasTrabajadas(double horasTrabajadas) {
        this.horasTrabajadas = horasTrabajadas;
    }

    public double getExtrasCorrientes() {
        return extrasCorrientes;
    }

    public void setExtrasCorrientes(double extrasCorrientes) {
        this.extrasCorrientes = extrasCorrientes;
    }

    public double getExtrasDobles() {
        return extrasDobles;
    }

    public void setExtrasDobles(double extrasDobles) {
        this.extrasDobles = extrasDobles;
    }

    


}
